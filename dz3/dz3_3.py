n=[7, 6, 4, 9, 5, 2, 1, 0, 3, 8]
kol=0
print(f'Исходный массив: {n}')
for i in n:
    #print(i) #поворот по часовой стрелке на 90 градусов
    kol += 1 #считаем количество чисел в строке
print("Поворачиваем массив на 90 градусов против часовой стрелки:")
while kol != 0: #задаем цикл, который пишет нам числа строки с конца по порядку
    kol -=1
    print(n[kol]) #выводим результат 