import numpy as np # вводим mathplotlib
x = ['Январь', 'Февраль', 'Март', 'Апрель', 'Май'] #задаем стоблцы
y = [2, 4, 3, 1, 7] #задаем их значения
from matplotlib import pyplot as plt
plt.bar(x, y, label="Распределение 23") #Параметр label позволяет задать название величины для легенды
plt.xlabel('Месяц года') #название оси x
plt.ylabel('Прибыль, в млн руб.') #название оси y
plt.title('Пример столбчатой диаграммы') #задаем название таблицы
plt.legend()
plt.show()